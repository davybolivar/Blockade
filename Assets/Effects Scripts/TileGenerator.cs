	void GenerateTile(){
	
		int rowSize = mapTile.GetLength (1);
		int colSize = mapTile.GetLength (0);
		rowSize--;
		colSize--;
		
		float origPosY = posY;
		
		for (int row = 0; row <= rowSize; row++) {
			
			for(int col = 0; col <= colSize; col++){
				SetTile (mapTile[col, row],posX,posY);
				posY -= tileHeight;
			}
			
			posY = origPosY;
			posX += tileWidth;
		}

	}

